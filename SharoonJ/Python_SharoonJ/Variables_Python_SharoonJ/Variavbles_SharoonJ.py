# ejemplos_tipos.py

print(" TIPOS DE DATOS ")

# ENTERO (int)

edad = 21
print("Edad:", edad)
print("Tipo:", type(edad))
print()

# DECIMAL (float)

altura = 1.75
print("Altura:", altura)
print("Tipo:", type(altura))
print()

# COMPLEJO (complex)

numero_complejo = 2 + 3j
print("Número complejo:", numero_complejo)
print("Tipo:", type(numero_complejo))
print()

# TEXTO (str)

mensaje = "Hola, estoy aprendiendo Python"
print("Mensaje:", mensaje)
print("Tipo:", type(mensaje))
print()

# BOOLEANO (bool)

es_estudiante = True
print("¿Es estudiante?:", es_estudiante)
print("Tipo:", type(es_estudiante))
print()

# LISTA (list)

numeros = [1, 2, 3, 4]
numeros.append(5)
print("Lista:", numeros)
print("Tipo:", type(numeros))
print()

# TUPLA (tuple)

coordenadas = (10, 20)
print("Tupla:", coordenadas)
print("Tipo:", type(coordenadas))
print()

# DICCIONARIO (dict)

persona = {"nombre": "Ana", "edad": 22}
print("Diccionario:", persona)
print("Tipo:", type(persona))
print()

# CONJUNTO (set)

valores = {1, 2, 2, 3}
print("Conjunto:", valores)
print("Tipo:", type(valores))
print()
